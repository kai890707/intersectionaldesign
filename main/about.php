                        本網站即為GI中文網之原文網站所推出的相關服務——「交織性設計卡片網」（簡稱卡片網）。該卡片網演示了設計過程中應考量的交織性因素，以利相關人員研發設計產品或服務時，避免忽略某些群體，而能符合更多人的需求。該卡片網提供了十二個交織性因素和名詞解釋，並將研發設計分為四個層次且設下問題供相關人員思考，更有豐富的案例供參考運用。
                    </div>
